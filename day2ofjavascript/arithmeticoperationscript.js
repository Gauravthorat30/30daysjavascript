let num1 = 10;
let num2 = 20;
let operation = "/";
if(operation == "+"){
    let ans = num1 + num2;
    console.log(ans)
}
else if(operation == "-"){
    let ans = num1 - num2;
    console.log(ans);
}
else if(operation == "*"){
    let ans = num1 * num2;
    console.log(ans);
}
else if(operation == "/"){
    let ans = num1 / num2;
    console.log(ans);
}
else{
    console.log("invalid operation")
}